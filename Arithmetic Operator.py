# Demonstration of AR operators
a=float(input("Enter the value of a:"))
b=float(input("Enter the value of b:"))

print("*"*65)

print("/t Arithmetic Operators")

print("*"*65)

print("\t Sum of {},{}=     {}".format(a,b,round((a+b),2)))
print("\t Sub of {},{}=     {}".format(a,b,round((a-b),2)))
print("\t Mul of {},{}=     {}".format(a,b,round((a*b),2)))
print("\t Div of {},{}=     {}".format(a,b,round((a/b),2)))
print("\t Floor Div of{},{}={}".format(a,b,round((a//b),2)))
print("\t Modulo of {},{}=  {}".format(a,b,round((a%b),2)))
print("\t Expo of {},{}=    {}".format(a,b,round((a**b),2)))




